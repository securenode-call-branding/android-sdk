    fun trustManager(): X509TrustManager? {
        val cert = certificate() ?: return null
        val keyStore = KeyStore.getInstance(KeyStore.getDefaultType()).apply {
            load(null)
            setCertificateEntry("prod-ca-2021", cert)
        }
        val tmf = TrustManagerFactory.getInstance(TrustManagerFactory.getDefaultAlgorithm()).apply {
            init(keyStore)
        }
        return tmf.trustManagers.filterIsInstance<X509TrustManager>().firstOrNull()
    }

    private fun certificate(): X509Certificate? {
        return try {
            val der = Base64.decode(DER_B64, Base64.DEFAULT)
            val cf = CertificateFactory.getInstance("X.509")
            ByteArrayInputStream(der).use { input ->
                cf.generateCertificate(input) as X509Certificate
            }
        } catch (_e: Exception) {
            null
        }
    }
